arrows = {
    5: up_arrow,
    4: up_right_arrow,
    3: right_arrow,
    2: down_right_arrow,
    1: down_arrow
}


def draw_arrow(graphics, trend, foreground, background, x_offset, y_offset):
    sprite = arrows.get(trend)
    for y in range(len(sprite)):
        for x_idx, x in enumerate(reversed(range(32))):
            bit_set = (sprite[y] >> x) & 1 > 0
            if bit_set :
                graphics.set_pen(foreground)
            else:
                graphics.set_pen(background)
            graphics.pixel(x_offset + x_idx, y_offset + y)


