    foreground = context.white()
    background = context.dark_background_blue()
    draw_arrow(context.graphics, 1, foreground, background, 10, 100)
    draw_arrow(context.graphics, 2, foreground, background, 50, 100)
    draw_arrow(context.graphics, 3, foreground, background, 90, 100)
